    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
        // TODO add your handling code here:
        for (int i=0;i< this.agenda.size();i++)
        {
            Agenda aux=(Agenda) agenda.get(i);
            if (aux.getTelefono().equals(Number.getText()))

            agenda.set(i,op.modificar(aux, Name.getText(), Number.getText(), Email.getText(), Alias.getText()));
        }
        this.show_elements(agenda);
        f.insert_element(agenda);
    }//GEN-LAST:event_EditActionPerformed

    private void RemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveActionPerformed
        // TODO add your handling code here:
        for (int i=0;i< agenda.size();i++)
        {
            Agenda aux=(Agenda) agenda.get(i);
            if (aux.getNombres().equals(nombre)){
                op.eliminar(agenda, aux);
            }
        }
        this.show_elements(agenda);
        f.insert_element(agenda);
            this.Alias.setText("");
            this.Email.setText("");
            this.Number.setText("");
            this.Name.setText("");

    }//GEN-LAST:event_RemoveActionPerformed

