    public void show_elements (List <Agenda> ag)
    {
        String table[][]=new String[agenda.size()][4];
        for (int i = 0;i < ag.size();i++)
        {
            table[i][0]= ag.get(i).getNombres();
            table[i][1]= ag.get(i).getTelefono();
            table[i][2]= ag.get(i).getCorreo_electronico();
            table[i][3]= ag.get(i).getAlias();
            
        }
          TableContacts.setModel(new javax.swing.table.DefaultTableModel(
           table,
            new String [] {
                "Nombre", "Número", "Email", "Alias"
            }
        ));
    }
